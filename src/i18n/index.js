const initial = storageService.getSettings().locale ||
  (typeof navigator !== 'undefined' && (navigator.language || '').toLowerCase().startsWith('id') ? 'id' : 'en')

export const locale = ref(DICT[initial] ? initial : 'en')
export const LOCALES = [{ id: 'en', label: 'English' }, { id: 'id', label: 'Indonesia' }]

export function setLocale(l) { if (DICT[l]) locale.value = l }

export function t(key, params) {
  let s = (DICT[locale.value] && DICT[locale.value][key]) || DICT.en[key] || key
  if (params) for (const k in params) s = s.replace('{' + k + '}', params[k])
  return s
}
